int	ft_atoi(char *str, int j)
{
	int		i;
	float	k;
	int		l;

	i = 0;
	k = 0.1;
	l = 0;
	while (i < j)
	{
		k = k * 10;
		i++;
	}
	i = str[i - 1] - '0';
	while (k != 1)
	{
		i = i + k * (str[l] - '0');
		k = k / 10;
		l++;
	}
	return (i);
}
