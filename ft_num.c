int	ft_num(char *str, int k)
{
	int	i;

	i = 0;
	if (k < 0)
		return (0);
	if (str[i] == '\0')
		return (0);
	else if (str[0] == '0' && str[1] != '\0')
		return (0);
	while (i < k)
	{
		if (!((str[i] >= '0') && (str[i] <= '9')))
			return (0);
		i++;
	}
	return (1);
}
